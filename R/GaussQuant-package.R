#' GaussQuant: Tidy Interface to QuantLib for Quantitative Finance
#'
#' GaussQuant provides reusable R interfaces to the SWIG version of
#' QuantLib for fixed income, derivatives, credit, risk analytics,
#' pricing engines, cashflows, and quantitative-finance workflows.
#'
#' GaussQuant is the formal successor to QuantLibGauss and uses
#' QuantLib directly. It does not depend on QuantLibGauss.
#'
#' LagrangeFinance provides the educational, presentation, table,
#' graph, and interpretation layer built on GaussQuant.
#'
#' Functions in the QuantLib and financial-calculation layer use the
#' suffix `_GQL`. Common helper functions use the suffix `_GQH`.
#'
